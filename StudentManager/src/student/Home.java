    public boolean isEmptyStudent() {
        if (TextField_stuName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student Name is missing");
            return false;
        }
        if (DateChooser_stuDOB.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Date Of Birth is missing");
            return false;
        }
        if (DateChooser_stuDOB.getDate().compareTo(new Date()) > 0) {
            JOptionPane.showMessageDialog(this, "No student from the future are allowed");
            return false;
        }
        if (TextField_stuEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Email is missing");
            return false;
        }
        if (!TextField_stuEmail.getText().matches("^.+@.+\\..+$")) {
            JOptionPane.showMessageDialog(this, "Invalid email address");
            return false;
        }
        if (TextField_stuPhone.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Phone Number is missing");
            return false;
        }
        if (TextField_stuPhone.getText().length() >= 15) {
            JOptionPane.showMessageDialog(this, "Phone Number too long");
            return false;
        }
        if (TextField_stuFather.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Father's Name is missing");
            return false;
        }
        if (TextField_stuMother.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mother's Name is missing");
            return false;
        }
        if (TextField_stuAddress.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Address is missing");
            return false;
        }
        if (imgPath == null) {
            JOptionPane.showMessageDialog(this, "Student must have image");
            return false;
        }
        return true;
    }

