    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser file = new JFileChooser();
        file.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("* image", "ipg", "gif", "png");
        file.addChoosableFileFilter(filter);
        int output = file.showSaveDialog(file);
        if (output == JFileChooser.APPROVE_OPTION) {
            File selectFile = file.getSelectedFile();
            String path = selectFile.getAbsolutePath();
            Label_stuImg.setIcon(imageAdjust(path, null));
            imgPath = path;
        }else {
            JOptionPane.showMessageDialog(this, "No Image selected");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void Table1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Table1MouseClicked
        model = (DefaultTableModel) Table1.getModel();
        rowIndex = Table1.getSelectedRow();
        TextField_stuID.setText(model.getValueAt(rowIndex, 0).toString());
        TextField_stuName.setText(model.getValueAt(rowIndex, 1).toString());
        try {
            Date date = new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(rowIndex, 2).toString());
            DateChooser_stuDOB.setDate(date);
        } catch (ParseException ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        }
        String gender = model.getValueAt(rowIndex, 3).toString();
        if (gender.equals("Male")) {
            ComboBox_stuGender.setSelectedIndex(0);
        }else {
            ComboBox_stuGender.setSelectedIndex(1);
        }
        TextField_stuEmail.setText(model.getValueAt(rowIndex, 4).toString());
        TextField_stuPhone.setText(model.getValueAt(rowIndex, 5).toString());
        TextField_stuFather.setText(model.getValueAt(rowIndex, 6).toString());
        TextField_stuMother.setText(model.getValueAt(rowIndex, 7).toString());
        TextField_stuAddress.setText(model.getValueAt(rowIndex, 8).toString());
        String path = model.getValueAt(rowIndex, 9).toString();
        imgPath = path;
        Label_stuImg.setIcon(imageAdjust(path, null));
    }//GEN-LAST:event_Table1MouseClicked

    private ImageIcon imageAdjust(String path, byte[] pic) {
        ImageIcon myImage = null;
        if (path != null) {
            myImage = new ImageIcon(path);
        }else {
            myImage = new ImageIcon(pic);
        }
        Image img = myImage.getImage();
        Image newImage = img.getScaledInstance(Label_stuImg.getWidth(), Label_stuImg.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon icon = new ImageIcon(newImage);
        return icon;
    }
    
