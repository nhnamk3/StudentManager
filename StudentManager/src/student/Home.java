    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (isEmptyStudent()) {
            int id = student.getMax();
            String name = TextField_stuName.getText();
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String date = dateFormat.format(DateChooser_stuDOB.getDate());
            String gender = ComboBox_stuGender.getSelectedItem().toString();
            String email = TextField_stuEmail.getText();
            String phone = TextField_stuPhone.getText();
            String father = TextField_stuFather.getText();
            String mother = TextField_stuMother.getText();
            String address = TextField_stuAddress.getText();
            if (student.isEmailExist(email)) {
                JOptionPane.showMessageDialog(this, "This email already exists");
            }else {
                if (student.isPhoneExist(phone)){
                    JOptionPane.showMessageDialog(this, "This phone number already exists");
                }else {
                    student.insert(id, name, date, gender, email, phone, father, mother, address, imgPath);
                    Table1.setModel(new DefaultTableModel(null, new Object[] {"Student ID", "Student Name", "Date Of Birth", "Gender", "Email", "Phone Number", "Father's Name", "Mother's Name", "Address", "Image Path"}));
                    student.getStudentValue(Table1, "");
                    clearStudent();
                }
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

